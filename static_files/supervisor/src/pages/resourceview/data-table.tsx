import {
  ColumnDef,
  flexRender,
  getCoreRowModel,
  useReactTable,
} from "@tanstack/react-table"

import {
  Table,
  TableBody,
  TableCell,
  TableHead,
  TableHeader,
  TableRow,
} from "@/components/ui/table"
import { useState } from "react"
import EditPeriodDialog from "./EditPeriod"
import { ResourceTableRow } from "@/types";


interface DataTableProps<TData extends ResourceTableRow, TValue> {
  asset: string;
  columns: ColumnDef<TData, TValue>[];
  data: TData[];
  // WARNING: This creates a strong coupling to the modal.
  onUpdate: () => void;
}

export function DataTable<TData extends ResourceTableRow, TValue>({
  asset,
  columns,
  data,
  onUpdate,
}: DataTableProps<TData, TValue>) {
  const table = useReactTable({
    data,
    columns,
    getCoreRowModel: getCoreRowModel(),
  })

  const [selectedRow, setSelectedRow] = useState<TData | null>(null);

  return (
    <>
      <div className="rounded-md border">
        <Table>
          <TableHeader>
            {table.getHeaderGroups().map((headerGroup) => (
              <TableRow key={headerGroup.id}>
                {headerGroup.headers.map((header) => {
                  return (
                    <TableHead key={header.id}>
                      {header.isPlaceholder
                        ? null
                        : flexRender(
                            header.column.columnDef.header,
                            header.getContext()
                          )}
                    </TableHead>
                  )
                })}
              </TableRow>
            ))}
          </TableHeader>
          <TableBody>
            {table.getRowModel().rows?.length ? (
              table.getRowModel().rows.map((row) => (
                <TableRow
                  key={row.id}
                  onClick={() => setSelectedRow(row.original)}
                >
                  {row.getVisibleCells().map((cell) => (
                    <TableCell key={cell.id}>
                      {flexRender(cell.column.columnDef.cell, cell.getContext())}
                    </TableCell>
                  ))}
                </TableRow>
              ))
            ) : (
              <TableRow>
                <TableCell colSpan={columns.length} className="h-24 text-center">
                  No results.
                </TableCell>
              </TableRow>
            )}
          </TableBody>
        </Table>
      </div>

      {selectedRow && (
        <EditPeriodDialog
          asset={asset}
          periodId={selectedRow.periodId}
          onClose={() => setSelectedRow(null)}
          onUpdate={onUpdate}
        />
      )}
    </>
  )
}

